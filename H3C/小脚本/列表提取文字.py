def extract_asset_info(data):
    """
    从数据列表中提取指定的资产信息字段
    """
    extracted_info = {}

    # 定义要查找的关键词
    keywords = ['查询', '借用挂账人']

    # 如果是二维列表，需要遍历每个子列表
    if isinstance(data, list) and len(data) > 0 and isinstance(data[0], list):
        for sublist in data:
            for i, item in enumerate(sublist):
                if item == '查询':
                    extracted_info['查询'] = item
                    # 下一个非空元素通常是资产编号
                    if i + 1 < len(sublist):
                        extracted_info['资产编号'] = sublist[i + 1]
                elif item == '借用挂账人':
                    extracted_info['借用挂账人'] = item
                    # 下一个元素通常是借用人信息
                    if i + 1 < len(sublist):
                        extracted_info['借用人信息'] = sublist[i + 1]
    # 如果是一维列表，使用原来的逻辑
    else:
        for i, item in enumerate(data):
            if item == '查询':
                extracted_info['查询'] = item
                if i + 1 < len(data):
                    extracted_info['资产编号'] = data[i + 1]
            elif item == '借用挂账人':
                extracted_info['借用挂账人'] = item
                if i + 1 < len(data):
                    extracted_info['借用人信息'] = data[i + 1]

    return extracted_info

